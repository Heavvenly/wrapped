function loadCard() {
  const cardData = currentCards[currentIndex];
  cardContent.innerHTML = "";

  // Set card content
  if (cardData.type === "prompt") {
    cardContent.innerHTML = `<h1 id="prompt">${cardData.text}</h1>`;
  } else if (cardData.type === "caption") {
    cardContent.innerHTML = `<h2>${cardData.title}</h2><p>${cardData.text}</p>`;
  } else if (cardData.type === "image") {
    cardContent.innerHTML = `<h3>${cardData.title}</h3><img src="${cardData.imageUrl}" alt="${cardData.title}" style="max-width:100%;border-radius:10px;">`;
  } else if (cardData.type === "answer") {
    cardContent.innerHTML = `<h1 id="answer">${cardData.text}</h1>`;
  }

  // Update background gradient
  const gradientContainer = document.querySelector(".gradient-container");
  if (gradientContainer) {
    gradientContainer.style.background = gradients[Math.floor(Math.random() * gradients.length)];
  }

  // Apply the "starting" animation for the first and last cards
  if (currentIndex === 0) {
    cardElement.classList.add("starting");
    setTimeout(() => cardElement.classList.remove("starting"), 800); // Remove after animation
  }
  if (currentIndex === currentCards.length - 1) {
    setTimeout(() => {
      cardElement.classList.add("ending");
    }, 5000); // Delays adding the "ending" class by 5000ms (5 seconds)
  } else {
    cardTimer = setTimeout(nextCard, 6500); // Start or reset the timer for automatic card transition
  }
}

function nextCard() {
  currentIndex++;
  const blurOverlay = document.getElementById('blur-overlay');

  if (currentIndex < currentCards.length) {
    blurOverlay.style.opacity = '1'; // Fade in blur

    cardElement.classList.remove("visible");
    cardElement.classList.add("hidden");

    setTimeout(() => {
      loadCard();
      cardElement.classList.remove("hidden");
      cardElement.classList.add("visible");

      blurOverlay.style.opacity = '0'; // Fade out blur
    }, 500);
  } else {
    blurOverlay.style.opacity = '0'; // Ensure blur is faded out
    cardContainer.innerHTML = "<h1 id='title'>Thanks for viewing your cards!</h1>";
  }
}

const cursor = document.querySelector('.custom-cursor');
const realcursor = document.querySelector('.realcustom-cursor');

let mouseX = 0,
  mouseY = 0;
let cursorX = 0,
  cursorY = 0;

// Update mouse position
document.addEventListener('mousemove', (e) => {
  mouseX = e.clientX;
  mouseY = e.clientY;
});

// Smooth follow effect
function animateCursor() {
  cursorX += (mouseX - cursorX) * 0.1;
  cursorY += (mouseY - cursorY) * 0.1;

  cursor.style.left = `${cursorX}px`;
  cursor.style.top = `${cursorY}px`;
  realcursor.style.left = `${mouseX}px`;
  realcursor.style.top = `${mouseY}px`;

  requestAnimationFrame(animateCursor);
}

// Add pop effect
document.addEventListener('mousemove', () => {
  cursor.classList.remove('active');
  clearTimeout(cursor.popTimeout);
  cursor.popTimeout = setTimeout(() => {
    cursor.classList.add('active');
  }, 500);
});

animateCursor();
